package DayFour.ShapeTask;

public class Point {
    private double sideX;
    private double sideY;

    public Point(double sideX, double sideY) {
        this.sideX = sideX;
        this.sideY = sideY;
    }

    public double getSideX() {
        return sideX;
    }

    public void setSideX(double sideX) {
        this.sideX = sideX;
    }

    public double getSideY() {
        return sideY;
    }

    public void setSideY(double sideY) {
        this.sideY = sideY;
    }
}
